import  java.util.Scanner;
public class Main {
    public static void main(String[] args) {
//        It is taking the input from the user.
        Scanner input = new Scanner(System.in);
//        This input.nextInt() allows to take int. from the User.
        System.out.println(input.nextInt());
//        This input.next() allows us to take first string from the user.
        System.out.println(input.next());
//        This input.nextLine() allows us to take whole line from the user.
        System.out.println(input.nextLine());
    }
}